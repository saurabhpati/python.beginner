import math;

print('acos of 0.5', math.acos(0.5));
print('asin of 0.5', math.asin(0.5));
print('atan of 0.5', math.atan(0.5));
print('hypot of 3 and 4 i.e. sqrt(3**2 + 4**2)', math.hypot(3,4));
print('sin of 0.5', math.sin(0.5));
print('cos of 0.5', math.cos(0.5));
print('tan of 0.5', math.tan(0.5));
print('pi radian is this many degrees=>', math.degrees(math.pi));
print('90 degrees is this many radians=>', math.radians(90));
